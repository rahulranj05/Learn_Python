num = int(input("Enter a number: "))
num2 = int(input("Enter another number: "))

# Arithmetic Operations

sum = num + num2  

diff = num - num2

prod = num * num2

div = num / num2

mod = num % num2

exp = num ** num2

floor_div = num // num2

print(f"Sum: {sum}")

print(f"Difference: {diff}")

print(f"Product: {prod}")

print(f"Division: {div}")

print(f"Modulus: {mod}")

print(f"Exponent: {exp}")

print(f"Floor Division: {floor_div}")