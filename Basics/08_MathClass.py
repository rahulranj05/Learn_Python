import math

print(math.pi)  

print(math.e)

print(math.tau)

print(math.inf)

print(math.nan) 

# Square root

result = math.sqrt(16)  # Returns the square root of 16
print(result)

# Ceiling and Floor

result = math.ceil(3.7)  # Round a number up
print(result)

result = math.floor(3.7)  # Rounds a number down
print(result)

# Factorial

result = math.factorial(5)  # Returns the factorial of 5
print(result)

