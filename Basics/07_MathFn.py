x = 3.14
y = -4
z = 10

# Rounding

result = round(x)  # Rounds to the nearest integer
print(result)  

# Absolute Value

result = abs(y)  # Returns the absolute value of y
print(result)

# Power

result = pow(z, 2)  # Raises x to the power of 2
print(result)

# max and min

result = max(x, y, z)  # Returns the maximum value among x, y, and z
print(result)

result = min(x, y, z)  # Returns the minimum value among x, y, and z
print(result)